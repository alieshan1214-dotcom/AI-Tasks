def dfs_tsp(graph, current, goal, visited, path, distance):
    path.append(current)
    visited.add(current)

    if current == goal:
        return path, distance

    for neighbor, weight in graph[current].items():
        if neighbor not in visited:
            result_path, total_dist = dfs_tsp(graph, neighbor, goal, visited, path[:], distance + weight)
            if result_path:
                return result_path, total_dist

    return None, 0

