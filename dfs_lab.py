def bfs_count(graph, start, goal):
    visited = []
    queue = [[start]]
    while queue:
        path = queue.pop(0)
        node = path[-1]
        if node not in visited:
            visited.append(node)
            if node == goal: return len(visited), path
            for neighbor in graph.get(node, {}):
                new_path = list(path)
                new_path.append(neighbor)
                queue.append(new_path)
    return len(visited), []

def dfs_count(graph, start, goal):
    visited = []
    stack = [[start]]
    while stack:
        path = stack.pop()
        node = path[-1]
        if node not in visited:
            visited.append(node)
            if node == goal: return len(visited), path
            # Reversed to simulate standard search order
            for neighbor in reversed(list(graph.get(node, {}).keys())):
                new_path = list(path)
                new_path.append(neighbor)
                stack.append(new_path)
    return len(visited), []

