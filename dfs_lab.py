def dfs_iterative(graph, start_node):
    visited = set()   # Set to keep track of visited nodes
    stack = [start_node]  # Step 1: Put start vertex on top of stack

    print("Following is the Depth-First Search:")

    # Step 4: Repeat until stack is empty
    while stack:
        # Step 2: Take the top item from the stack
        vertex = stack.pop()

        if vertex not in visited:
            print(vertex, end=" ")
            visited.add(vertex) # Add to visited list

            # Step 3: Create list of adjacent nodes
            # We reverse the neighbors so they are processed in the same
            # order as the recursive version (5 -> 3 -> 2...)
            for neighbour in reversed(graph[vertex]):
                if neighbour not in visited:
                    stack.append(neighbour)

